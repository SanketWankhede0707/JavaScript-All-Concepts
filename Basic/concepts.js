// Variables in JAVASCRIPT :
// let, const, var
// 'let': use 'let' when you need a variable that can change value(mutable)
// 'const': use 'const' when you need a variable that can't change value(immutable)
// 'var': use 'var' when you need a variable that can change value(mutable) but its generally better to use 'let' or 'const'.
// Example's :

let age= 20; // mutable
const name= 'your name'; // immutable
var old_way='avoide using it'; // mutable but don't use this.

